int	ft_antidote(int i, int j, int k)
{
	if ((i == k || i == j) || (i > j && i < k) || (i < j && i > k))
		return (i);
	if (j == k || (j > i && j < k) || (j > k && j < i))
		return (j);
	return (k);
}
